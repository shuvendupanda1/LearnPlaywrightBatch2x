//Bitwise AND Operator ( & )
let x = 5; 
let y = 3; 
console.log(x & y);
//Bitwise OR Operator ( | )
console.log(x | y);
//Bitwise XOR Operator ( ^ )
console.log(x ^ y);
//Bitwise NOT Operator ( ~ )
console.log(~x);
//Left Shift Operator ( << )
console.log(x << 1);
//Right Shift Operator ( >> )
console.log(x >> 1);

//Zero Fill Right Shift Operator ( >>> )
console.log(x >>> 1);
let q = 4; 
let s = -1 
console.log(q >>> 1); 
console.log(s >>> 4);

