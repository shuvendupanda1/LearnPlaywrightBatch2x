let a = 34;
let result = a++;
console.log(result);
console.log(a); 

// Output:
// 34
// 35

// In the above code, the value of `a` is first assigned to `result`, and then `a` is incremented by 1. Therefore, `result` gets the original value of `a` (34), and `a` becomes 35 after the post-increment operation.