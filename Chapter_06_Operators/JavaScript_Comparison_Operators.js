//Java script Comparison Operators

//Equal to(==)
const a = 5;
const b = '5';
console.log(a == b); // true

//StrictEqual(===)
console.log(a === b); // false

//Not Equal(!=)
console.log(a != b); // false

//Strict Not Equal(!==)
console.log(a !== b); // true

//Greater Than(>)
console.log(a > b); // false

//Less Than(<)
console.log(a < b); // false

//Greater Than or Equal(>=)
console.log(a >= b); // true

//Less Than or Equal(<=)
console.log(a <= b); // true