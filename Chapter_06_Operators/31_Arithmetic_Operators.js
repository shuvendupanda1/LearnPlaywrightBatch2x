// Number + Number => Addition 
let x = 1 + 2;
console.log( x );

// Number + String => Concatenation
let y =  5 + "hello"; 
console.log( y );


// Subtraction (-) Operator

// Number - Number => Subtraction 
let x1 = 10 - 7;
console.log( x1 );

let y1 = "Hello" - 1; 
console.log( y1 );

// Multiplication (*) Operator
// Number * Number => Multiplication
