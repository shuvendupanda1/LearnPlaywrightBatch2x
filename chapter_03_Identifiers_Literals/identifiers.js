// ============================================
// JavaScript Identifiers - Examples
// ============================================

// ----- Valid Identifiers -----
let name = "Alice";
let _userName = "bob_123";
let $price = 99.99;
let firstName2 = "John";
let _total_amount = 500;
let _data99 = [1, 2, 3];

console.log(name, _userName, $price, firstName2, _total_amount, _data99);


// ----- Case Sensitivity -----
let myVar = 10;
let myvar = 20;

console.log(myVar);   // 10
console.log(myvar);   // 20


// ----- Identifiers can be used in expressions -----
let count = 0;
let _isLoggedIn = true;
let $result = count + 100;

console.log($result);        // 100
console.log(_isLoggedIn);    // true
